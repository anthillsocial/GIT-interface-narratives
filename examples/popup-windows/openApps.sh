# COMMANDS TO AUTOMATICALY OPEN WINDOWS

# USING THE COMMANDLINE ON MAC OSx
man open      # Instructions for the 'open command'
open ~/Desktop  # Open the Desktop folder
open https://theanthillsocial.co.uk # Open a URL
echo "Hello" | open -f # Open TestEdit with this text in a file
ls | open -f  # List contents of the current directory
open *        # Open all files in the current directory

# Run a sequence of these commands in a bash script!